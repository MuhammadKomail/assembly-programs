.model small
.stack 100h
.DATA 
.CODE

START:
MOV AX,@DATA
MOV DS,AX

L1:
MOV AH,2CH
INT 21H
MOV AL,CH
CALL CONV
CALL DISP
MOV DL,':'
MOV AH,06H
INT 21H
MOV AL,CL
CALL CONV
CALL DISP
MOV DL,':'
MOV AH,06H
INT 21H
MOV DL,0DH
MOV AH,06H
INT 21H
JMP L1
MOV AH,4CH
INT 21H
DISP PROC NEAR
PUBLIC DISP
MOV BL,AH
MOV DL,AL
ADD DL,30H
MOV AH,06H
INT 21H
MOV DL,BL
ADD DL,30H
MOV AH,06H
INT 21H
RET
DISP ENDP
CONV PROC NEAR
PUBLIC CONV
MOV AH,00H
MOV BH,0AH
DIV BH
RET
CONV ENDP

END START
